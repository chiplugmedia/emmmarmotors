<script>
// ===================== COUNTER =====================
function animateNumberCompact(el, end, duration, type = '') {
  let start = 0;
  let startTime = null;

  function formatNumber(n) {
    if (n >= 1000) {
      return Math.floor(n / 1000) + 'k+';
    }
    return n.toString();
  }

  function step(timestamp) {
    if (!startTime) startTime = timestamp;
    const progress = Math.min((timestamp - startTime) / duration, 1);
    const ease = 1 - Math.pow(1 - progress, 3);
    const value = Math.floor(ease * (end - start) + start);

    if (type === '$') {
      el.textContent = `$${formatNumber(value)}`;
    } else if (type === 'h') {
      el.textContent = `${value}h`;
    } else {
      el.textContent = formatNumber(value);
    }

    if (progress < 1) {
      requestAnimationFrame(step);
    }
  }

  requestAnimationFrame(step);
}

// ===================== START COUNTERS =====================
function startCounters() {
  const users = document.getElementById('stat-users');
  const payout = document.getElementById('stat-payout');
  const withdrawal = document.getElementById('stat-withdrawal');

  if (users.dataset.done) return;

  animateNumberCompact(users, 250000, 800);       // 250k+
  animateNumberCompact(payout, 200000, 1000, '$'); // $200k+
  animateNumberCompact(withdrawal, 24, 600, 'h');  // 24h

  users.dataset.done = "true";
}

// ===================== OBSERVER =====================
const observer = new IntersectionObserver((entries, obs) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      startCounters();
      obs.disconnect();
    }
  });
}, { threshold: 0.3 });

const section = document.getElementById('stats-section');
if (section) observer.observe(section);
</script>
